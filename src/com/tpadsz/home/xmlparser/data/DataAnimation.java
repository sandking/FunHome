package com.tpadsz.home.xmlparser.data;

public class DataAnimation
{
	private int id ;
	private int frameID;
	private int moveID;
	private int zoomID;
	private int rotateID;
	private int alphaID;
	private int colorID;
	private int order;
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public int getFrameID()
	{
		return frameID;
	}
	public void setFrameID(int frameID)
	{
		this.frameID = frameID;
	}
	public int getMoveID()
	{
		return moveID;
	}
	public void setMoveID(int moveID)
	{
		this.moveID = moveID;
	}
	public int getZoomID()
	{
		return zoomID;
	}
	public void setZoomID(int zoomID)
	{
		this.zoomID = zoomID;
	}
	public int getRotateID()
	{
		return rotateID;
	}
	public void setRotateID(int rotateID)
	{
		this.rotateID = rotateID;
	}
	public int getAlphaID()
	{
		return alphaID;
	}
	public void setAlphaID(int alphaID)
	{
		this.alphaID = alphaID;
	}
	public int getColorID()
	{
		return colorID;
	}
	public void setColorID(int colorID)
	{
		this.colorID = colorID;
	}
	public int getOrder()
	{
		return order;
	}
	public void setOrder(int order)
	{
		this.order = order;
	}
	
}
