package com.tpadsz.home.xmlparser.animation;

public class DataAlpha
{
	private int id;
	private int type;
	private int alphaStart;
	private int alphaEnd;
	private int speedType;
	private int duration;
	private int loop;
	private int alphaG;
	private int toOrBy;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public int getType()
	{
		return type;
	}

	public void setType(int type)
	{
		this.type = type;
	}

	public int getAlphaStart()
	{
		return alphaStart;
	}

	public void setAlphaStart(int alphaStart)
	{
		this.alphaStart = alphaStart;
	}

	public int getAlphaEnd()
	{
		return alphaEnd;
	}

	public void setAlphaEnd(int alphaEnd)
	{
		this.alphaEnd = alphaEnd;
	}

	public int getSpeedType()
	{
		return speedType;
	}

	public void setSpeedType(int speedType)
	{
		this.speedType = speedType;
	}

	public int getDuration()
	{
		return duration;
	}

	public void setDuration(int time)
	{
		this.duration = time;
	}

	public int getLoop()
	{
		return loop;
	}

	public void setLoop(int loop)
	{
		this.loop = loop;
	}

	public int getAlphaG()
	{
		return alphaG;
	}

	public void setAlphaG(int alphaG)
	{
		this.alphaG = alphaG;
	}

	public int getToOrBy()
	{
		return toOrBy;
	}

	public void setToOrBy(int toOrBy)
	{
		this.toOrBy = toOrBy;
	}

}
