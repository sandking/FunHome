package com.tpadsz.home.xmlparser.animation;

public class DataFrame
{
	private int id;
	private int num;
	private String src;
	private String fileName;
	private int rate;
	private int loop;
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public int getNum()
	{
		return num;
	}
	public void setNum(int num)
	{
		this.num = num;
	}
	public String getSrc()
	{
		return src;
	}
	public void setSrc(String src)
	{
		this.src = src;
	}
	public String getFileName()
	{
		return fileName;
	}
	public void setFileName(String fileName)
	{
		this.fileName = fileName;
	}
	public int getRate()
	{
		return rate;
	}
	public void setRate(int rate)
	{
		this.rate = rate;
	}
	public int getLoop()
	{
		return loop;
	}
	public void setLoop(int loop)
	{
		this.loop = loop;
	}
	
	
}
