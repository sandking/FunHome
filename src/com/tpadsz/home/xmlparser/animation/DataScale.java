package com.tpadsz.home.xmlparser.animation;

public class DataScale
{
	private int id;
	private int type;
	private float x_rate;
	private float y_rate;
	private int x;
	private int y;
	private int speed_type;
	private int duration;
	private int loop;
	private int zoomG;
	private int toOrBy;
	
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public int getType()
	{
		return type;
	}
	public void setType(int type)
	{
		this.type = type;
	}
	public float getX_rate()
	{
		return x_rate;
	}
	public void setX_rate(float x_rate)
	{
		this.x_rate = x_rate;
	}
	public float getY_rate()
	{
		return y_rate;
	}
	public void setY_rate(float y_rate)
	{
		this.y_rate = y_rate;
	}
	public int getX()
	{
		return x;
	}
	public void setX(int x)
	{
		this.x = x;
	}
	public int getY()
	{
		return y;
	}
	public void setY(int y)
	{
		this.y = y;
	}
	public int getSpeed_type()
	{
		return speed_type;
	}
	public void setSpeed_type(int speed_type)
	{
		this.speed_type = speed_type;
	}
	public int getDuration()
	{
		return duration;
	}
	public void setDuration(int duration)
	{
		this.duration = duration;
	}
	public int getLoop()
	{
		return loop;
	}
	public void setLoop(int loop)
	{
		this.loop = loop;
	}
	public int getZoomG()
	{
		return zoomG;
	}
	public void setZoomG(int zoomG)
	{
		this.zoomG = zoomG;
	}
	public int getToOrBy()
	{
		return toOrBy;
	}
	public void setToOrBy(int toOrBy)
	{
		this.toOrBy = toOrBy;
	}
	
	

}
